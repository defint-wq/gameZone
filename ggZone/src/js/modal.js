function openEditModal() {
  const modal = document.getElementById("editProfileModal");
  if (modal) {
    modal.style.display = "flex";
  }
}

function closeEditModal() {
  const modal = document.getElementById("editProfileModal");
  if (modal) {
    modal.style.display = "none";
  }
}

